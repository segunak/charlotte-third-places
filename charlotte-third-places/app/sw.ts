/// <reference no-default-lib="true" />
/// <reference lib="esnext" />
/// <reference lib="webworker" />
import { defaultCache } from "@serwist/turbopack/worker";
import type { PrecacheEntry, SerwistGlobalConfig } from "serwist";
import { NetworkOnly, Serwist } from "serwist";

declare global {
  interface WorkerGlobalScope extends SerwistGlobalConfig {
    __SW_MANIFEST: (PrecacheEntry | string)[] | undefined;
  }
}

declare const self: ServiceWorkerGlobalScope;

const AZURE_PHOTOS_ORIGIN = "https://thirdplacesdata.blob.core.windows.net";

const runtimeCaching = [
  {
    // Photo blobs are immutable at their URL, but caching many large responses in
    // the service worker can put pressure on iOS WebViews. Let the browser fetch them directly.
    matcher: ({ url }: { url: URL }) => url.origin === AZURE_PHOTOS_ORIGIN && url.pathname.startsWith("/photos/"),
    method: "GET" as const,
    handler: new NetworkOnly(),
  },
  ...defaultCache,
];

const serwist = new Serwist({
  precacheEntries: self.__SW_MANIFEST,
  skipWaiting: true,
  clientsClaim: true,
  navigationPreload: true,
  runtimeCaching,
  fallbacks: {
    entries: [
      {
        url: "/~offline",
        matcher({ request }) {
          return request.destination === "document";
        },
      },
    ],
  },
});

serwist.addEventListeners();
